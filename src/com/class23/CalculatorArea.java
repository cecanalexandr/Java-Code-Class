package com.class23;

public class CalculatorArea {
	public void CalculatorArea(int a) {
		System.out.println("Calculate the area of the square"+(a*a));
		System.out.println(a*a);
	}
	public void CalculatorArea(int a,int b) {
		System.out.println("Calculate the area of the rectangle"+(a*b));
		System.out.println(a*b);
	}
	public void CalculatorArea(int a,int b,int c) {
		System.out.println("Calculate the area of the box"+(a*b*c));
		System.out.println(a*b*c);
	}
	

}
